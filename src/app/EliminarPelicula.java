    private void btnEliminarIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarIdActionPerformed
        // TODO add your handling code here:

        if ( txtId.getText().equals("") ) {

            JOptionPane.showMessageDialog(this, "Ingrese ID de Pelicula");

        } else {

            try {

                Pelicula oPelicula = new Pelicula();
                
                oPelicula.eliminarPelicula(Integer.parseInt(txtId.getText()), mapaPeliculas);

                txtId.setText("");
                txtNombreID.setText("");
                txtAnioId.setText("");
                txtDirectorId.setText("");
                txtGeneroId.setText("");
                txtPrecioVentaID.setText("");
                txtPrecioArriendoId.setText("");

                JOptionPane.showMessageDialog(this, "Pelicula eliminada correctamente");


            }   catch (SQLException ex) {
                Logger.getLogger(EliminarPelicula.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_btnEliminarIdActionPerformed

