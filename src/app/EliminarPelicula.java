    private void btnBuscarIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarIDActionPerformed
        // TODO add your handling code here:

        if ( txtId.getText().equals("") ){

            JOptionPane.showMessageDialog(this, "Ingrese ID de pelicula");

        } else {
            //System.out.println(""+Integer.parseInt(txtId.getText()));

            Pelicula oPelicula = new Pelicula();

            oPelicula = oPelicula.buscarPelicula(mapaPeliculas, Integer.parseInt(txtId.getText()));

            if ( oPelicula != null ) {

                txtNombreID.setText(oPelicula.getNombre());
                txtAnioId.setText(String.valueOf(oPelicula.getAnio()));
                txtDirectorId.setText(oPelicula.getDirector());
                txtGeneroId.setText(oPelicula.getGenero());
                txtPrecioVentaID.setText(String.valueOf(oPelicula.getPrecioVenta()));
                txtPrecioArriendoId.setText(String.valueOf(oPelicula.getPrecioArriendo()));

            } else {

                JOptionPane.showMessageDialog(this, "Ocurrio un problema");

                txtNombreID.setText("");
                txtAnioId.setText("");
                txtDirectorId.setText("");
                txtGeneroId.setText("");
                txtPrecioVentaID.setText("");
                txtPrecioArriendoId.setText("");

            }

            
        }

    }//GEN-LAST:event_btnBuscarIDActionPerformed

